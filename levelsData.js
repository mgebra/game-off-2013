var CellType = {
	Ground: 0,
	Grass: 10,
	Glue: 20,
	Stone: 30,
	Ice: 40,
	Bomb: 50,
	Exit: 60
}

function getCellType (n) {
	if (n < 10) {
		return CellType.Ground;
	} else if (n < 20) {
		return CellType.Grass;
	} else if (n < 30) {
		return CellType.Glue;
	} else if (n < 40) {
		return CellType.Stone;
	} else if (n < 50) {
		return CellType.Ice;
	} else if (n < 60) {
		return CellType.Bomb;
	} else {
		return CellType.Exit;
	}
}

